package com.onesoft.day3_StringMethods;

public class StringConcat {
	public static void main(String[] args) {
		String name = "Raj";
		String fullName = name.concat("Kumar");
		System.out.println("The fullname is :" + fullName);
		
		String myName = "Balaji";
		String myFullName = myName.concat("Kaliyamoorthy");
		System.out.println("My fullname is "+myFullName);
	}
}
